#Create a program that takes two numbers as input and prints whether
# the first number is greater than, less than, or equal to the second number.

num_1 = int(input("Please enter the number1:-"))
num_2 = int(input("Please enter the number2:-"))
print(num_1 > num_2)
print(num_1 < num_2)
print(num_1 >= num_2)
print(num_1 <= num_2)
print(num_1 == num_2)
